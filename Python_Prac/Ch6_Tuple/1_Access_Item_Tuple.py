#Example 1  by index
tuple1=("Shahy","khan","Ali",1,2,3)
tuple2=(1,2,3,4,5)
print("Item at 1 index in tuple1: ",tuple1[1])
print("Item at 3 index in tuple2: ",tuple2[3])

#Example 2 by negative index
print("Item at last index in tuple : ",tuple1[-1])

#Example 3 Extracting a Subtuple from a Tuple
# The slice operator extracts a subtuple from the original tuple.

print("Item from index 0 to 3 in tuple1: ",tuple1[0:4])
print("Item from index 0 to 3 in tuple1: ",tuple1[0:-2])

